num1 = 5
num2 = 10

print(f'{num1} é maior que {num2}? R: {num1 > num2}')
print(f'{num1} é menor que {num2}? R: {num1 < num2}')
print(f'{num1} é igual a {num2}? R: {num1 == num2}')
print(f'{num1} é maior ou igual a {num2}? R: {num1 >= num2}')
print(f'{num1} é menor ou igual a {num2}? R: {num1 <= num2}')
print(f'{num1} é diferente de {num2}? R: {num1 != num2}')