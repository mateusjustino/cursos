'''
+ = soma
- = subtração
* = multiplicação
/ = divisão
// = é uma divisão inteira, só pega o numero inteiro, sem casas decimais
** = potenciação
% = retorna o resto de uma divisão inteira
'''

print(f'Soma(+): 10 + 10 = {10+10}')
print(f'Subtração(-): 10 - 10 = {10-10}')
print(f'Multiplicação(*): 10 * 10 = {10*10}')
print(f'Potenciação(**): 10 ** 10 = {10**10}')
print(f'Divisão(/): 10 / 3 = {10/3}')
print(f'Divisão inteira(//): 10 // 3 = {10//3}')
print(f'Resto de uma divisão(%): 10 % 3 = {10%3}')



